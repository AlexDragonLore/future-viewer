    private static IEnumerable<TarotCard> BuildMajorArcana()
    {
        foreach (var m in Majors)
        {
            yield return new TarotCard
            {
                Id = m.Number + 1,
                Name = m.Name,
                Suit = CardSuit.MajorArcana,
                Number = m.Number,
                DescriptionUpright = m.Up,
                DescriptionReversed = m.Rev,
                ImagePath = $"/cards/major/{m.Number:D2}.jpg",
                NameEn = m.NameEn,
                ShortUpright = m.ShortUp,
                ShortReversed = m.ShortRev,
                UprightKeywords = m.UpKw.ToList(),
                ReversedKeywords = m.RevKw.ToList(),
                SuggestedTone = m.Tone,
                Aliases = m.Aliases?.ToList()
            };
        }
    }

