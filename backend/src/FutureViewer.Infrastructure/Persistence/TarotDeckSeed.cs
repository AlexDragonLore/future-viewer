    public static IReadOnlyList<TarotCard> BuildDeck()
    {
        var cards = new List<TarotCard>(78);
        cards.AddRange(BuildMajorArcana());
        cards.AddRange(BuildMinorArcana(Wands, startId: 23));
        cards.AddRange(BuildMinorArcana(Cups, startId: 37));
        cards.AddRange(BuildMinorArcana(Swords, startId: 51));
        cards.AddRange(BuildMinorArcana(Pentacles, startId: 65));
        return cards;
    }

    public static IReadOnlyList<DeckVariant> BuildDeckVariants(IEnumerable<TarotCard> deck)
    {
        var list = new List<DeckVariant>(78 * 5);
        foreach (var card in deck)
        {
            foreach (var (deckType, template) in DeckTemplates)
            {
                list.Add(new DeckVariant
                {
                    CardId = card.Id,
                    DeckType = deckType,
                    VariantNote = string.Format(template, card.NameEn)
                });
            }
        }
        return list;
    }

