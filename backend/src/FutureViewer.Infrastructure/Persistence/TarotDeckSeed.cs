public static class TarotDeckSeed
{
    public static IReadOnlyList<TarotCard> BuildDeck()
    {
        var cards = new List<TarotCard>(78);
        cards.AddRange(BuildMajorArcana());
        cards.AddRange(BuildMinorArcana(CardSuit.Wands, "Жезлы", startId: 23));
        cards.AddRange(BuildMinorArcana(CardSuit.Cups, "Кубки", startId: 37));
        cards.AddRange(BuildMinorArcana(CardSuit.Swords, "Мечи", startId: 51));
        cards.AddRange(BuildMinorArcana(CardSuit.Pentacles, "Пентакли", startId: 65));
        return cards;
    }

