    private static IEnumerable<TarotCard> BuildMinorArcana(SuitInfo suit, int startId)
    {
        for (var i = 0; i < Ranks.Length; i++)
        {
            var r = Ranks[i];
            yield return new TarotCard
            {
                Id = startId + i,
                Name = $"{r.RankRu} {suit.NameRu}",
                Suit = suit.Suit,
                Number = r.Num,
                DescriptionUpright = $"{r.ShortUp} Стихия: {suit.Element}.",
                DescriptionReversed = $"{r.ShortRev} Стихия: {suit.Element}.",
                ImagePath = $"/cards/{suit.Slug}/{r.Num:D2}.jpg",
                NameEn = $"{r.RankEn} of {suit.NameEn}",
                ShortUpright = r.ShortUp,
                ShortReversed = r.ShortRev,
                UprightKeywords = r.BaseUp.Concat(suit.Flavor).ToList(),
                ReversedKeywords = r.BaseRev.ToList(),
                SuggestedTone = suit.Tone,
                Aliases = null
            };
        }
    }
